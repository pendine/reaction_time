CREATE TABLE IF NOT EXISTS UserInfo (
      INDX_NUMB BIGSERIAL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY
    , JOIN_DATE TEXT
    , OAUTH_ID  TEXT
    , NICK_NAME	VARCAHR(30)
    , HASH_CODE	NUMBER
    , EXIT      BOOLEAN
    , LOGN_DATE	TEXT
    );
CREATE INDEX ON UserInfo (INDX_NUMB, OAUTH_ID, NICK_NAME, HASH_CODE);
COMMENT ON TABLE UserInfo IS '소셜로그인유저 정보 테이블';
COMMENT ON COLUMN UserInfo.INDX_NUMB    IS '정렬순서';
COMMENT ON COLUMN UserInfo.JOIN_DATE    IS '가입일자(YYYYMMDDHH24MISS)';
COMMENT ON COLUMN UserInfo.OAUTH_ID     IS '소셜로그인아이디';
COMMENT ON COLUMN UserInfo.NICK_NAME    IS '닉네임';
COMMENT ON COLUMN UserInfo.HASH_CODE    IS '해시코드';
COMMENT ON COLUMN UserInfo.EXIT	        IS '탈퇴여부';
COMMENT ON COLUMN UserInfo.LOGN_DATE    IS '마지막 로그인 일자(YYYYMMDDHH24MISS)';
---
CREATE TABLE IF NOT EXISTS UserRecord (
      RECD_DATE	    TEXT
    , OAUTH_ID	    TEXT
    , NICK_NAME	    VARCAHR(30)
    , HASH_CODE	    NUMBER
    , TRY_NUMB	    NUMBER
    , TRY_PER_NUMB	NUMBER
    , TRY_PER_SEC	NUMBER
    , TRY_AVRG_SEC	NUMBER
    );
CREATE INDEX ON UserRecord(RECD_DATE, TRY_NUMB, TRY_PER_NUMB);
COMMENT ON TABLE UserRecord IS '소셜로그인유저 기록정보';
COMMENT ON COLUMN UserRecord.RECD_DATE	    IS '일자(YYYYMMDDHH24MISS)';
COMMENT ON COLUMN UserRecord.OAUTH_ID	    IS '소셜로그인아이디';
COMMENT ON COLUMN UserRecord.NICK_NAME	    IS '닉네임';
COMMENT ON COLUMN UserRecord.HASH_CODE	    IS '해시코드';
COMMENT ON COLUMN UserRecord.TRY_NUMB	    IS '시도회차 번호';
COMMENT ON COLUMN UserRecord.TRY_PER_NUMB   IS '시도회차의 개별번호';
COMMENT ON COLUMN UserRecord.TRY_PER_SEC    IS '시도회차의 개별 반응 시간';
